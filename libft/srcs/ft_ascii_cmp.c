#include "libft.h"

#include <stdio.h>

int			ft_ascii_cmp(char *s1, char *s2)
{
	while (*s1 && *s2 && *s1 == *s2)
	{
		s1++;
		s2++;
	}
	return ((*s1 < *s2) ? 1 : -1);
}
